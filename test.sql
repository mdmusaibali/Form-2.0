create table publications(
    name varchar(100),
    department varchar(100),
    title varchar(100),
    typeOfPublication varchar(100),
    typeOfConference varchar(100),
    titleOfJournal varchar(100),
    volumeNumber varchar(100),
    issueNumber varchar(100),
    pageNumber varchar(100),
    yearNumber varchar(100),
    indexing varchar(100),
    h_index varchar(100),
    impactFactor varchar(100),
    uploadFileName varchar(100)
);